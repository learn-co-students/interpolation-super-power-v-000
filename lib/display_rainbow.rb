def display_rainbow(input)
  colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
  if input == colors
    puts "R: #{input[0]}, O: #{input[1]}, Y: #{input[2]}, G: #{input[3]}, B: #{input[4]}, I: #{input[5]}, V: #{input[6]}\n"
  end
end 