colors = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]

def display_rainbow(rainbow_color)
  puts "R: #{rainbow_color[0]}, O: #{rainbow_color[1]}, Y: #{rainbow_color[2]}, G: #{rainbow_color[3]}, B: #{rainbow_color[4]}, I: #{rainbow_color[5]}, V: #{rainbow_color[6]}"
end


display_rainbow(colors)
