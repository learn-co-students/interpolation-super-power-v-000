def display_rainbow(colors)
  color_key = ["R", "O", "Y", "G", "B", "I", "V"]
  puts "#{color_key[0]}: #{colors[0]}, #{color_key[1]}: #{colors[1]}, #{color_key[2]}: #{colors[2]}, #{color_key[3]}: #{colors[3]}, #{color_key[4]}: #{colors[4]}, #{color_key[5]}: #{colors[5]}, #{color_key[6]}: #{colors[6]}"
end